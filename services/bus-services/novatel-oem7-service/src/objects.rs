/// Receiver status - a list of active flag strings
#[derive(Clone)]
pub struct ReceiverStatus(pub ReceiverStatusFlags);

/// Custom scalar for ReceiverStatus - serializes the bitflags as a list of strings
#[Scalar]
impl ScalarType for ReceiverStatus {
    fn parse(_value: Value) -> async_graphql::InputValueResult<Self> {
        Err(async_graphql::InputValueError::custom(
            "ReceiverStatus is not supported as an input type",
        ))
    }

    fn to_value(&self) -> Value {
        Value::List(
            self.0
                .to_vec()
                .iter()
                .map(|flag| Value::String(flag.to_owned()))
                .collect(),
        )
    }
}

